using System.Drawing;

namespace Disco.Models
{
   //================================================================================================//
   public 
   class Wish
   {
      //---------------------------------------------------------------------------------------------//
      public
      Wish()
      {
      }
      //---------------------------------------------------------------------------------------------//
      public
      Color
      Color
      { get; set; }
      //---------------------------------------------------------------------------------------------//
      public 
      string 
      Name
      { get; set; }
      //---------------------------------------------------------------------------------------------//
      public
      string 
      ImageLocation 
      { get; set; }
      //---------------------------------------------------------------------------------------------//
      public
      bool
      IsGranted
      { get; set; }
      //---------------------------------------------------------------------------------------------//
      public
      bool
      IsPending
      { get; set; }
      //---------------------------------------------------------------------------------------------//
      public
      static
      Wish
      GetRandom()
      {
         Wish wish = new Wish();
         wish.Color = Randomizer.GetColor();
         wish.Name = Randomizer.GetAlphaString(5, 30);
         wish.ImageLocation = Randomizer.GetImageLocation();
         wish.IsGranted = Randomizer.GetBoolean(.1);
         if (!wish.IsGranted)
            wish.IsPending = Randomizer.GetBoolean(.25);
         return wish;
      }
      //---------------------------------------------------------------------------------------------//
   }
   //================================================================================================//
}